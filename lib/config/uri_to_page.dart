Page? uriToPage(Uri? uri) {
  final u = uri?.toString();
  final key = UriKey(uri);
  if (u != null) {
    if (u.startsWith("/404")) {
      return Screen404(key: key).toMaterialPage();
    }
    if (u.startsWith("/main")) {
      return ScreenMain(key: key).toMaterialPage();
    }
    return Screen404(key: key).toMaterialPage();
  }
}
